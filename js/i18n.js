let curLang='en';

function t(key,...args){
  const tr=LANGS[curLang]||LANGS.en;
  const val=tr[key]??LANGS.en[key]??key;
  return typeof val==='function'?val(...args):val;
}

function tCat(cat){const k=CAT_MAP[cat];return k?t(k):cat;}

function applyLang(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key=el.getAttribute('data-i18n');
    el.textContent=t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el=>{
    el.title=t(el.getAttribute('data-i18n-title'));
  });
  // Rebuild all dynamic elements with new language
  buildTabs();buildPalette();rebuildImportedPalette();buildDots();
  refreshList();refreshEdit();
  if(fpVisible)drawFloorPlan();
  const sn=document.getElementById('sn');
  if(sn)sn.textContent=t('pageOf',curP+1,pages.length);
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.classList.toggle('on',b.dataset.lang===curLang);
  });
}

function buildLangBar(){
  const bar=document.getElementById('lang-bar');bar.innerHTML='';
  Object.entries(LANG_META).forEach(([code,meta])=>{
    const b=document.createElement('button');
    b.className='lang-btn'+(code===curLang?' on':'');
    b.dataset.lang=code;b.textContent=meta.label;b.title=meta.name;
    b.onclick=()=>{curLang=code;applyLang();};
    bar.appendChild(b);
  });
}
